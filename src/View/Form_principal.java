    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // Recuperando o caminho do arquivo selecionado
        Localizar_Arquivo_Controller control = new Localizar_Arquivo_Controller();

        // abre formulario Jdialog para localizar o arquivo .RE com as infomações
        String caminho = localizarArquivoLayout();

        if (caminho == null) { // verifica se o caminho estiver vazio
            JOptionPane.showMessageDialog(this, "Nenhum arquivo foi selecionado", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // recuperando todas as linhas do arquivo selecionado
            List<String> ListaArquivoRe = control.lerArquivoSelecionado(caminho);

            Pessoas_Controller pessoas = new Pessoas_Controller();
            pessoas.buscarPisPasepBaseDados(ListaArquivoRe, caminho);

        }
    }//GEN-LAST:event_jButton1ActionPerformed


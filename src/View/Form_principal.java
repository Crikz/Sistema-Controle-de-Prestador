    /**
     * Recupera o caminho do arquivo
     *
     * @return caminho - Caminho do arquivo selecionado pelo usuario
     */
    private String localizarArquivoLayout() {
        String caminho = null;

        JFileChooser buscar = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Arquivo .RE", "RE"); // arquivo .RE aceito pela SEFIP/GFIP
        buscar.setFileFilter(filtro);
        int opcao = buscar.showOpenDialog(null);

        if (opcao == buscar.getApproveButtonMnemonic()) {
            caminho = buscar.getSelectedFile().getPath();
//            System.out.println("caminho: " + caminho);
        }
        return caminho;
    }


